int ft_isprint(int c)
{
    if ((c >= 32 && c <= 126))
        return(1);
    else
        return(0);
}

#include <stdio.h>
int main(void)
{
    char c = 'z';
    printf("%d", ft_isprint(c));
    return(0);
}